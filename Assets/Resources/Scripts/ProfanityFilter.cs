    public static string ReplaceCurseWords(this string value)
    {
        string cleanWord = value;
        cleanWord = cleanWord.ToLower();

        for (int i = 0; i < m_CurseWords.Length; i++)
        {
            string curseWord = m_CurseWords[i];
            if(cleanWord.Contains(curseWord))
            {
                //Debug.Log(curseWord);
                cleanWord = cleanWord.Replace(curseWord, m_CensorWord);
            }
        }

        //Debug.Log(cleanWord);
        return cleanWord;
    }
